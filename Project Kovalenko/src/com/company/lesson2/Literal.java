package com.company.lesson2;

public class Literal {
	public static void main(String[] args) {
		System.out.println("Логический литерал - " + false);
		System.out.println("Строковый литерал");
		System.out.println("Символьный литерал - " + 's');
		System.out.println("Двоичный литерал - '0b101' = " + 0b101);
		System.out.println("Восьмиричный литерал - '0101' =  " + 0101);
		System.out.println("Десятичный литерал - " + 3);
		System.out.println("Шестнадцатиричный литерал - '0x101' =  " + 0x101);
		System.out.println("Литерал типа float - " + 3.45f);
		System.out.println("Литерал типа double - " + 3.45);
	}
}
